const mongoose = require("mongoose");

const attendamceSchema = new mongoose.Schema({
  employeeId: {
    type: String,
    required: true,
  },
  employeeName: {
    type: String,
    required: true,
  },
  date: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    required: true,
  },
});

exports.Attendance = mongoose.model("Attendance", attendamceSchema);
